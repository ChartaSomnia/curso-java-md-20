public class Wrapper {
    public static void main(String[] args) {
        // Son Wrapper classes que envuelven tipos de datos
        // primitivos en objetos.

        //Integer - int
        //Boolean - boolean
        //Character - cha
        
        
        Integer objeto = 5; // Autoboxing. Convierte el int 5 en un Integer.
        int numero = objeto;
        System.out.println(numero);

    }
}
