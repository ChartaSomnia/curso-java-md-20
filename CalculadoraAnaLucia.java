package practicas.calculadora;

public class CalculadoraAnaLucia {
    public static void main(String args[]){
        // Las variables de valo que calcularemos

        int valor1 = 20;
        int valor2 = 30;

        // Suma
        int suma = valor1+ valor2;
        System.out.println("el valor de mi suma es " +suma);

        //Resta
        int resta = valor1- valor2;
        System.out.println("el valor de mi resta es " -resta);

        //Multiplicacion
int multiplicacion = valor1* valor2;
System.out.println("el valor de mi multiplicacion es " *multiplicacion);

        //Division
        int Division = valor1/ valor2;
        System.out.println("el valor de mi division es " /division);

    }
}

