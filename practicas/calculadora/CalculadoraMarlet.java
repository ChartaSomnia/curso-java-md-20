package practicas.calculadora;

public class CalculadoraMarlet {
    public static void main(String[] args) {
     // Las variables de valor que calcularemos
     
     int valor1 = 60;
     int valor2 = 20;

     //Suma
     int suma = valor1 + valor2;
     System.out.println("El valor de mi suma es"+suma);

     //Resta
     int resta = valor1 - valor2;
     System.out.println("El valor de mi resta es"+resta);

     //Multiplicacion
     int multiplicacion = valor1 * valor2;
     System.out.println("El valor de mi multiplicacion"+multiplicacion);

     //Division
     int division = valor1 / valor2;
     System.out.println("El valor de mi division"+division);
    }
}

